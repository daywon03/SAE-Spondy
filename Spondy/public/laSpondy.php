            <h1>Spondylarthrite Ankylosante (SA)</h1><!--Titre de l'article-->

            <p>La <strong>spondylarthrite ankylosante</strong> (SA) est une <strong>maladie inflammatoire chronique</strong> qui affecte principalement la colonne vertébrale et les articulations sacro-iliaques (au niveau des fesses). Elle résulte d'un dérèglement du système immunitaire, entraînant une inflammation quasi permanente. La SA fait partie des <span class="tooltip">maladies auto-immunes<span class="tooltiptext">Maladie où le système immunitaire attaque par erreur les cellules saines du corps.</span></span>, ce qui signifie que le corps attaque ses propres tissus par erreur.</p>

            <div class="note">
                <p><span class="important">Note :</span> Même si la SA ne peut pas être guérie, il existe des traitements efficaces pour en freiner l'évolution et éviter les complications graves comme l'ankylose (raideur) ou les déformations.</p>
            </div>

            <h2>Une maladie fréquente mais souvent tardivement diagnostiquée</h2>

            <p>La SA touche environ 150 000 adultes en France, principalement les jeunes adultes âgés de 20 à 30 ans. Contrairement à ce qu'on a longtemps cru, les hommes et les femmes sont touchés de manière presque égale, bien que le diagnostic soit souvent plus difficile chez les femmes en raison de symptômes plus subtils.</p>

            <h2>Origines de la maladie</h2>

            <p>Les causes exactes de la SA sont encore mal comprises, mais il existe une <span class="tooltip">prédisposition génétique<span class="tooltiptext">Facteurs héréditaires qui augmentent la probabilité de développer la maladie.</span></span> qui peut être déclenchée par des facteurs environnementaux tels que :</p>

            <ul>
                <li>Événements émotionnels</li>
                <li>Agents infectieux</li>
                <li>Facteurs environnementaux</li>
            </ul>

            <p>Bien que la SA ne soit pas une maladie strictement héréditaire, certaines recherches sont en cours pour mieux comprendre les gènes impliqués dans son développement.</p>

            <h2>Symptômes et manifestations</h2>

            <h3>Inflammation des enthèses</h3>

            <p>La SA provoque une inflammation dans une partie spécifique des articulations appelée <span class="tooltip">enthèse<span class="tooltiptext">Zone où les tendons ou ligaments se fixent à l'os.</span></span> — le point où les tendons ou les ligaments s'attachent à l'os. Cela peut affecter la colonne vertébrale, les talons et parfois d'autres articulations.</p>

            <h3>Une maladie douloureuse et fatigante</h3>

            <p>Les premiers signes de la SA incluent des <strong>douleurs fluctuantes</strong> et une <strong>raideur matinale</strong>. Ces symptômes apparaissent souvent pendant la nuit et nécessitent une période de "dérouillage" au réveil qui peut durer de quelques minutes à plusieurs heures. La <strong>fatigue chronique</strong> est également fréquente.</p>

            <div class="note">
                <p><span class="important">Note :</span> Certaines personnes ne ressentent que peu ou pas de douleurs mais peuvent tout de même s'ankyloser.</p>
            </div>

            <h2>Évolution de la SA</h2>

            <h3>Une progression par poussées</h3>

            <p>La maladie évolue souvent par <strong>poussées</strong>, avec des périodes où les symptômes s'aggravent, suivies de rémissions. À long terme, la maladie peut entraîner une raideur permanente de la colonne vertébrale par fusion des vertèbres.</p>

            <h3>Complications</h3>

            <p>Outre la raideur de la colonne, la SA peut toucher d'autres articulations comme les hanches, les épaules, ou encore les poignets, voire entraîner des problèmes respiratoires si la cage thoracique est affectée. Des manifestations extra-articulaires comme le <span class="tooltip">psoriasis<span class="tooltiptext">Maladie de peau caractérisée par des plaques rouges et des squames.</span></span>, les <span class="tooltip">uvéites<span class="tooltiptext">Inflammation de l'uvée, une partie de l'œil.</span></span> (inflammations oculaires) ou des troubles intestinaux (maladie de Crohn) sont également possibles.</p>

            <h2>Diagnostic</h2>

            <p>Le diagnostic de la SA repose principalement sur l’examen clinique, les antécédents familiaux, et des tests sanguins comme la recherche de l’antigène <span class="tooltip">HLA-B27<span class="tooltiptext">Antigène génétique lié à un risque accru de SA.</span></span>. Cependant, les premiers signes radiologiques peuvent être absents, retardant parfois le diagnostic.</p>

            <h3>Critères d’Amor</h3>

            <p>Les <strong>critères d’Amor</strong> permettent d'aider au diagnostic. Voici certains signes majeurs :</p>

            <ul>
                <li>Douleurs nocturnes et/ou raideur matinale durant plus de 30 minutes (1 point)</li>
                <li>Douleurs des fesses : d'un seul côté (1 point), ou alternées (2 points)</li>
                <li>Inflammation visible aux radiographies des articulations sacro-iliaques (3 points)</li>
                <li>Antécédents familiaux de SA ou test HLA-B27 positif (2 points)</li>
                <li>Réponse positive aux anti-inflammatoires (2 points)</li>
            </ul>

            <h2>Traitement et prise en charge</h2>

            <p>Bien qu’il n'existe pas de cure définitive pour la SA, plusieurs traitements permettent de gérer efficacement la douleur, de maintenir la mobilité et de réduire les déformations :</p>

            <ul>
                <li>Anti-inflammatoires non stéroïdiens (AINS)</li>
                <li>Thérapies biologiques ciblées</li>
                <li>Kinésithérapie régulière pour maintenir la souplesse des articulations</li>
            </ul>

            <p>Un suivi régulier avec un <strong>rhumatologue</strong> est essentiel pour adapter le traitement au fil du temps. En cas de complications oculaires (comme une uvéite), il est important de consulter un ophtalmologue rapidement.</p>

            <h2>Conseils pour vivre avec la SA</h2>

            <p>Vivre avec une maladie chronique comme la SA peut être un défi. Il est important d'impliquer son entourage et d'apprendre à adapter ses activités quotidiennes pour préserver sa qualité de vie. En cas de besoin, un soutien psychologique peut être utile pour gérer les aspects émotionnels de la maladie.</p>

